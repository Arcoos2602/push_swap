#include "../includes/libft.h"

void	*ft_memmove(void *dest, const void *src, size_t len)
{
	char			*d;
	const char		*s;

	d = dest;
	s = src;
	if (d == s)
		return (dest);
	if (d < s)
		while (len--)
			*d++ = *s++;
	else
	{
		s += (len - 1);
		d += (len - 1);
		while (len--)
			*d-- = *s--;
	}
	return (dest);
}
